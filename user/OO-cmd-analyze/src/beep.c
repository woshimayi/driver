#include "beep.h"
#include "cmd.h"
#include "array.h"

/******************************************************************************
* 函数介绍： 打开蜂鸣器函数
* 输入参数： 无
* 输出参数： 无
* 返回值 ：  无
* 备    注： 无 
******************************************************************************/
static void beep_on(void)
{
 	BEEP = 0;
}

/******************************************************************************
* 函数介绍： 关闭蜂鸣器函数
* 输入参数： 无
* 输出参数： 无
* 返回值 ：  无
* 备    注： 无 
******************************************************************************/
static void beep_off(void)
{
 	BEEP = 1;
}

/******************************************************************************
* 函数介绍： 蜂鸣器初始化函数
* 输入参数： 无
* 输出参数： 无
* 返回值 ：  无
* 备    注： 注册命令、初始化硬件 
******************************************************************************/
void beep_init(void)
{
	/* 填充命令结构体数组 */
	CMD beep_cmds[] = {
	 	{"beep on", beep_on},
		{"beep off", beep_off}
	};
	
	/* 注册命令 */
	register_cmds(beep_cmds, ARRAY_SIZE(beep_cmds));
	
	/* 初始化硬件 */
	beep_off();	
}

